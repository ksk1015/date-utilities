import { describe, it, expect } from 'vitest'
import { rowsOfCalendar } from './rowsOfCalendar'

describe('rowsOfCalendar', () => {
  it('2024年 日曜始まり', () => {
    expect(rowsOfCalendar(new Date('2024-01'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-02'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-03'))).toBe(6)
    expect(rowsOfCalendar(new Date('2024-04'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-05'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-06'))).toBe(6)
    expect(rowsOfCalendar(new Date('2024-07'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-08'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-09'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-10'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-11'))).toBe(5)
    expect(rowsOfCalendar(new Date('2024-12'))).toBe(5)
  })
  it('2024年 月曜始まり', () => {
    expect(rowsOfCalendar(new Date('2024-01'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-02'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-03'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-04'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-05'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-06'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-07'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-08'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-09'), 1)).toBe(6)
    expect(rowsOfCalendar(new Date('2024-10'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-11'), 1)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-12'), 1)).toBe(6)
  })
  it('2024年 土曜始まり', () => {
    expect(rowsOfCalendar(new Date('2024-01'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-02'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-03'), 6)).toBe(6)
    expect(rowsOfCalendar(new Date('2024-04'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-05'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-06'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-07'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-08'), 6)).toBe(6)
    expect(rowsOfCalendar(new Date('2024-09'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-10'), 6)).toBe(5)
    expect(rowsOfCalendar(new Date('2024-11'), 6)).toBe(6)
    expect(rowsOfCalendar(new Date('2024-12'), 6)).toBe(5)
  })
})
